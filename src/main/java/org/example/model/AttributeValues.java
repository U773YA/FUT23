package org.example.model;

public class AttributeValues {

    private String shortcut;
    private String gk_shortcut;
    private Integer chem_change;
    private String id;
    private String stat_num;
    private String name;
    private String gk_stat_name;
    private String type;
    private Integer value;
    private Boolean max;
    private String sqlCol;

    public String getShortcut() {
        return shortcut;
    }

    public void setShortcut(String shortcut) {
        this.shortcut = shortcut;
    }

    public String getGk_shortcut() {
        return gk_shortcut;
    }

    public void setGk_shortcut(String gk_shortcut) {
        this.gk_shortcut = gk_shortcut;
    }

    public Integer getChem_change() {
        return chem_change;
    }

    public void setChem_change(Integer chem_change) {
        this.chem_change = chem_change;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStat_num() {
        return stat_num;
    }

    public void setStat_num(String stat_num) {
        this.stat_num = stat_num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGk_stat_name() {
        return gk_stat_name;
    }

    public void setGk_stat_name(String gk_stat_name) {
        this.gk_stat_name = gk_stat_name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }

    public Boolean getMax() {
        return max;
    }

    public void setMax(Boolean max) {
        this.max = max;
    }

    public String getSqlCol() {
        return sqlCol;
    }

    public void setSqlCol(String sqlCol) {
        this.sqlCol = sqlCol;
    }
}
